// ── Three.js 3D Visualization ──
let _threeRAF = null;

function initThreeScene() {
  const canvas = document.getElementById('planCanvas3d');
  if (!canvas || typeof THREE === 'undefined') return;

  const scene = new THREE.Scene();
  scene.background = new THREE.Color(0xf0e9dc);
  scene.fog = new THREE.FogExp2(0xf0e9dc, 0.009);

  const getSize = () => ({ w: canvas.clientWidth || 800, h: canvas.clientHeight || 540 });
  let { w, h } = getSize();

  const camera = new THREE.PerspectiveCamera(48, w / h, 0.1, 300);

  const renderer = new THREE.WebGLRenderer({ canvas, antialias: true });
  renderer.setSize(w, h, false);
  renderer.setPixelRatio(Math.min(window.devicePixelRatio, 2));
  renderer.shadowMap.enabled = true;
  renderer.shadowMap.type = THREE.PCFSoftShadowMap;

  // ── Layout constants (scaled from SVG / 18) ──
  // Left block:  SVG x=25..514  → 3D x≈1.4..28.6
  // Right block: SVG x=586..1075 → 3D x≈32.6..59.7
  // 4 rows of lots at SVG rows_y=[83,235,417,569] → 3D z≈4.6,13,23.2,31.6
  // Vertical road (gap 72px): 3D x≈28.6..32.6  (center ≈ 30.6)
  // Internal road between row pairs: SVG y=367..417 → 3D z≈20.4..23.2

  const SCALE = 18;
  const leftBlkX0  = 25 / SCALE, leftBlkX1  = 514 / SCALE;   // ≈ 1.4, 28.6
  const rightBlkX0 = 586 / SCALE, rightBlkX1 = 1075 / SCALE;  // ≈ 32.6, 59.7
  const siteXC     = (leftBlkX1 + rightBlkX0) / 2;            // ≈ 30.6 (road centre)
  const rowZ       = [83, 235, 417, 569].map(v => (v + 66) / SCALE); // lot centres
  const siteZC     = (rowZ[1] + rowZ[2]) / 2;                 // ≈ 21.5 (internal road centre)
  const siteZTop   = (83 - 28) / SCALE;                       // top outer road
  const siteZBot   = (701 + 22) / SCALE;                      // entrance road centre

  // Ground
  const groundMat = new THREE.MeshLambertMaterial({ color: 0xddd0b4 });
  const ground = new THREE.Mesh(new THREE.PlaneGeometry(90, 60), groundMat);
  ground.rotation.x = -Math.PI / 2;
  ground.position.set(siteXC, -0.05, siteZC);
  ground.receiveShadow = true;
  scene.add(ground);

  // ── Roads ──
  const roadMat = new THREE.MeshLambertMaterial({ color: 0xc4b8a4 });
  // Top outer road (horizontal)
  const topRoad = new THREE.Mesh(new THREE.BoxGeometry(rightBlkX1 - leftBlkX0 + 2, 0.06, 1.8), roadMat);
  topRoad.position.set(siteXC, 0.02, siteZTop);
  scene.add(topRoad);
  // Entrance road (horizontal, bottom)
  const botRoad = new THREE.Mesh(new THREE.BoxGeometry(rightBlkX1 - leftBlkX0 + 2, 0.06, 2.8), roadMat);
  botRoad.position.set(siteXC, 0.02, siteZBot);
  scene.add(botRoad);
  // Internal horizontal road (between sector pairs)
  const midRoad = new THREE.Mesh(new THREE.BoxGeometry(rightBlkX1 - leftBlkX0 + 2, 0.06, (417 - 367) / SCALE), roadMat);
  midRoad.position.set(siteXC, 0.02, (367 + 417) / 2 / SCALE);
  scene.add(midRoad);
  // Vertical road (between blocks)
  const vRoad = new THREE.Mesh(new THREE.BoxGeometry((rightBlkX0 - leftBlkX1), 0.06, siteZBot - siteZTop + 2), roadMat);
  vRoad.position.set(siteXC, 0.02, (siteZTop + siteZBot) / 2);
  scene.add(vRoad);
  // Alleys (thin horizontal strips inside blocks)
  const alleyMat = new THREE.MeshLambertMaterial({ color: 0xcfc3af });
  [[83+132, 83+132+20], [417+132, 417+132+20]].forEach(([y0, y1]) => {
    const zm = (y0 + y1) / 2 / SCALE;
    [leftBlkX0 + (leftBlkX1-leftBlkX0)/2, rightBlkX0 + (rightBlkX1-rightBlkX0)/2].forEach(ax => {
      const al = new THREE.Mesh(new THREE.BoxGeometry(leftBlkX1-leftBlkX0, 0.04, (y1-y0)/SCALE), alleyMat);
      al.position.set(ax, 0.01, zm);
      scene.add(al);
    });
  });

  // Roundabout at central intersection
  const rtMat = new THREE.MeshLambertMaterial({ color: 0xb8aa96 });
  const rCentre = new THREE.Mesh(new THREE.CylinderGeometry(1.2, 1.2, 0.12, 16), rtMat);
  rCentre.position.set(siteXC, 0.06, siteZC);
  scene.add(rCentre);
  const rGreen = new THREE.Mesh(new THREE.CylinderGeometry(0.55, 0.55, 0.18, 16), new THREE.MeshLambertMaterial({ color: 0x5e7d42 }));
  rGreen.position.set(siteXC, 0.09, siteZC);
  scene.add(rGreen);

  // ── Buildings ──
  const buildingMeshes = [];
  LOTS_DATA.forEach(lot => {
    const pos = get3DPosition(lot);
    const bw  = (SVG_CFG.lotW - 6) / SCALE;
    const bd  = (SVG_CFG.lotH - 8) / SCALE;
    const bh  = Math.max(2.2, lot.surface / 34);

    const isAvail = lot.status === 'available';
    const mat = new THREE.MeshPhongMaterial({
      color:    isAvail ? 0xc49030 : 0x3c342c,
      shininess: isAvail ? 50 : 10,
      specular:  isAvail ? 0x4a3800 : 0x0a0806,
    });

    const mesh = new THREE.Mesh(new THREE.BoxGeometry(bw, bh, bd), mat);
    mesh.position.set(pos.x, bh / 2, pos.z);
    mesh.castShadow = true;
    mesh.receiveShadow = true;
    mesh.userData = {
      lotId:      lot.id,
      colorBase:  isAvail ? 0xc49030 : 0x3c342c,
      colorHover: isAvail ? 0xe0b040 : 0x5a4e48,
    };
    scene.add(mesh);

    // Flat roof cap
    const roofMat = new THREE.MeshLambertMaterial({ color: isAvail ? 0xa07228 : 0x252018 });
    const roof = new THREE.Mesh(new THREE.BoxGeometry(bw + 0.2, 0.2, bd + 0.2), roofMat);
    roof.position.set(pos.x, bh + 0.1, pos.z);
    scene.add(roof);

    buildingMeshes.push(mesh);
  });

  // ── Mosque (community facility marker) ──
  const mBase = new THREE.Mesh(new THREE.BoxGeometry(2, 1.8, 2), new THREE.MeshPhongMaterial({ color: 0x8c7a50, shininess: 20 }));
  mBase.position.set(siteXC, 0.9, siteZTop - 3.5);
  scene.add(mBase);
  const mDome = new THREE.Mesh(new THREE.SphereGeometry(0.75, 12, 8, 0, Math.PI*2, 0, Math.PI/2), new THREE.MeshPhongMaterial({ color: 0x7a6a3e, shininess: 30 }));
  mDome.position.set(siteXC, 1.8, siteZTop - 3.5);
  scene.add(mDome);

  // ── Trees ──
  const foliageMat = new THREE.MeshLambertMaterial({ color: 0x527040 });
  const trunkMat   = new THREE.MeshLambertMaterial({ color: 0x6b4e2c });
  const addTree = (tx, tz) => {
    const trunk = new THREE.Mesh(new THREE.CylinderGeometry(0.1, 0.14, 0.8, 5), trunkMat);
    trunk.position.set(tx, 0.4, tz);
    scene.add(trunk);
    const top = new THREE.Mesh(new THREE.ConeGeometry(0.58, 1.5, 6), foliageMat);
    top.position.set(tx, 1.5, tz);
    scene.add(top);
  };
  // Along top outer road
  for (let c = 0; c < 8; c++) {
    addTree(leftBlkX0  + (c + 0.5) * (leftBlkX1  - leftBlkX0)  / 8, siteZTop);
    addTree(rightBlkX0 + (c + 0.5) * (rightBlkX1 - rightBlkX0) / 8, siteZTop);
  }
  // Along entrance road
  for (let c = 0; c < 8; c++) {
    addTree(leftBlkX0  + (c + 0.5) * (leftBlkX1  - leftBlkX0)  / 8, siteZBot);
    addTree(rightBlkX0 + (c + 0.5) * (rightBlkX1 - rightBlkX0) / 8, siteZBot);
  }
  // Along vertical road (both sides)
  for (let r = 0; r < 4; r++) {
    addTree(leftBlkX1  + 0.7, rowZ[r]);
    addTree(rightBlkX0 - 0.7, rowZ[r]);
  }

  // ── Lighting ──
  scene.add(new THREE.AmbientLight(0xfff8e8, 0.9));
  const sun = new THREE.DirectionalLight(0xfff0d0, 1.6);
  sun.position.set(-30, 70, -25);
  sun.castShadow = true;
  sun.shadow.mapSize.width  = 2048;
  sun.shadow.mapSize.height = 2048;
  sun.shadow.camera.left   = -80;
  sun.shadow.camera.right  =  80;
  sun.shadow.camera.top    =  80;
  sun.shadow.camera.bottom = -80;
  sun.shadow.camera.near   = 1;
  sun.shadow.camera.far    = 250;
  scene.add(sun);
  const fill = new THREE.DirectionalLight(0xd0e8ff, 0.4);
  fill.position.set(40, 25, 50);
  scene.add(fill);

  // ── Camera orbital control ──
  const target = new THREE.Vector3(siteXC, 0, siteZC);
  let theta = Math.PI * 0.55;
  let phi   = 0.16;     // very low for dramatic intro
  let radius = 130;     // start far away
  let isDragging = false;
  let prevXY = { x: 0, y: 0 };
  const raycaster = new THREE.Raycaster();
  const mouse2d   = new THREE.Vector2();
  let hoveredMesh = null;

  // Intro animation
  const introEnd   = { theta: Math.PI * 0.60, phi: 0.72, radius: 80 };
  const introDur   = 4500;
  let introStart   = null;
  let introRunning = true;

  const easeOutCubic = t => 1 - Math.pow(1 - t, 3);

  function updateCamera() {
    camera.position.x = target.x + radius * Math.sin(phi) * Math.sin(theta);
    camera.position.y = target.y + radius * Math.cos(phi);
    camera.position.z = target.z + radius * Math.sin(phi) * Math.cos(theta);
    camera.lookAt(target);
  }

  // Mouse/touch handlers
  const getXY = e => e.touches ? { x: e.touches[0].clientX, y: e.touches[0].clientY } : { x: e.clientX, y: e.clientY };

  canvas.addEventListener('mousedown', e => { isDragging = true; prevXY = getXY(e); introRunning = false; });
  canvas.addEventListener('touchstart', e => { isDragging = true; prevXY = getXY(e); introRunning = false; }, { passive: true });
  window.addEventListener('mouseup',  () => { isDragging = false; });
  window.addEventListener('touchend', () => { isDragging = false; });

  const onMove = e => {
    const xy = getXY(e);
    if (isDragging) {
      theta -= (xy.x - prevXY.x) * 0.007;
      phi = Math.max(0.08, Math.min(1.45, phi - (xy.y - prevXY.y) * 0.007));
      prevXY = xy;
    }
    const rect = canvas.getBoundingClientRect();
    mouse2d.x = ((xy.x - rect.left) / canvas.clientWidth ) * 2 - 1;
    mouse2d.y = -((xy.y - rect.top)  / canvas.clientHeight) * 2 + 1;
  };
  canvas.addEventListener('mousemove', onMove);
  canvas.addEventListener('touchmove', onMove, { passive: true });

  canvas.addEventListener('wheel', e => {
    e.preventDefault();
    radius = Math.max(22, Math.min(130, radius + e.deltaY * 0.06));
    introRunning = false;
  }, { passive: false });

  canvas.addEventListener('click', e => {
    if (isDragging) return;
    raycaster.setFromCamera(mouse2d, camera);
    const hits = raycaster.intersectObjects(buildingMeshes);
    if (hits.length) openLotPanel(hits[0].object.userData.lotId);
  });

  window.addEventListener('resize', () => {
    const s = getSize();
    camera.aspect = s.w / s.h;
    camera.updateProjectionMatrix();
    renderer.setSize(s.w, s.h, false);
  });

  // ── Animation loop ──
  function animate(now) {
    _threeRAF = requestAnimationFrame(animate);

    // Intro sweep
    if (introRunning) {
      if (!introStart) introStart = now;
      const t = Math.min(1, (now - introStart) / introDur);
      const e = easeOutCubic(t);
      theta  = Math.PI * 0.55 + (introEnd.theta - Math.PI * 0.55) * e;
      phi    = 0.18 + (introEnd.phi - 0.18) * e;
      radius = 115 + (introEnd.radius - 115) * e;
      if (t >= 1) introRunning = false;
    } else if (!isDragging) {
      theta += 0.0018; // gentle auto-rotation
    }

    updateCamera();

    // Hover detection
    raycaster.setFromCamera(mouse2d, camera);
    const hits = raycaster.intersectObjects(buildingMeshes);
    if (hoveredMesh) {
      hoveredMesh.material.color.setHex(hoveredMesh.userData.colorBase);
      hoveredMesh.material.emissive.setHex(0x000000);
      hoveredMesh = null;
      canvas.style.cursor = 'grab';
    }
    if (hits.length) {
      hoveredMesh = hits[0].object;
      hoveredMesh.material.color.setHex(hoveredMesh.userData.colorHover);
      hoveredMesh.material.emissive.setHex(0x1a0e00);
      canvas.style.cursor = 'pointer';
    }

    renderer.render(scene, camera);
  }

  requestAnimationFrame(animate);
}

