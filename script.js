const config = {
  whatsappNumber: "",
};

const getCurrentStrings = () => content[document.documentElement.lang] || content.en;

const normalizePhoneForWa = (value) => (value || "").replace(/[^\d]/g, "");

const buildWhatsAppUrl = (number, message) => {
  const normalized = normalizePhoneForWa(number);
  if (!normalized) return "#";
  return `https://wa.me/${normalized}?text=${encodeURIComponent(message)}`;
};

const updateWhatsAppCtas = () => {
  const formCta = document.getElementById("formWhatsappCta");
  const strings = getCurrentStrings();
  const message = strings.hero_title || content.en.hero_title;
  const href = buildWhatsAppUrl(config.whatsappNumber, message);

  [formCta].forEach((node) => {
    if (!node) return;
    node.href = href;
    node.style.pointerEvents = href === "#" ? "none" : "auto";
    node.style.opacity = href === "#" ? "0.5" : "1";
  });
};

const applyLanguage = (lang) => {
  const strings = content[lang];
  if (!strings) return;

  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.body.classList.toggle("is-rtl", lang === "ar");

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (strings[key]) {
      node.textContent = strings[key];
    }
  });

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("is-active", btn.dataset.lang === lang);
  });

  const languageSelect = document.querySelector('select[name="language"]');
  if (languageSelect) {
    languageSelect.value = lang;
  }

  updateWhatsAppCtas();
};

const attachLanguageSwitch = () => {
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", () => {
      applyLanguage(btn.dataset.lang);
    });
  });
};

