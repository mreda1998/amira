// ── SVG Layout Config ──
// viewBox: 0 0 1100 900
// Two main rectangular blocks separated by a vertical access road (72px).
// Each block: 8 cols × 4 rows of lots, with:
//   • rows 1-2 back-to-back (alley between them, 20px)
//   • rows 3-4 back-to-back (alley between them, 20px)
//   • a main internal road between the two pairs (50px)
// Sector 1 (Zone A) = left block rows 1-2 │ Sector 2 (Zone B) = right block rows 1-2
// Sector 4 (Zone D) = left block rows 3-4 │ Sector 3 (Zone C) = right block rows 3-4
const SVG_CFG = {
  viewBox: '0 0 1100 900',
  lotW: 55,        // lot width in SVG units
  lotH: 132,       // lot height (depth) in SVG units
  lotGap: 7,       // gap between lots in same row
  alleyH: 20,      // alley between back-to-back rows
  innerRoadH: 50,  // internal road between sector pairs
  outerRoadH: 28,  // top/bottom outer road
  entranceH: 45,   // main entrance road (bottom)
  vertRoadW: 72,   // vertical road between left/right blocks
  leftBlockX: 25,  // left edge of left block
  blockW: 8 * 55 + 7 * 7,  // = 440+49 = 489px
  // Row Y positions (top edge of each row)
  rows_y: [83, 235, 417, 569],
  // [row0_y=83, row1_y=83+132+20=235, row2_y=235+132+50=417, row3_y=417+132+20=569]
};

function getLotSVGRect(zone, idx) {
  // Zone A → left block rows 0-1
  // Zone D → left block rows 2-3
  // Zone B → right block rows 0-1
  // Zone C → right block rows 2-3
  const blockX  = (zone === 'A' || zone === 'D')
    ? SVG_CFG.leftBlockX
    : SVG_CFG.leftBlockX + SVG_CFG.blockW + SVG_CFG.vertRoadW;
  const rowOff  = (zone === 'A' || zone === 'B') ? 0 : 2;
  const localRow = rowOff + Math.floor(idx / 8);
  const col      = idx % 8;
  return {
    x: blockX + col * (SVG_CFG.lotW + SVG_CFG.lotGap),
    y: SVG_CFG.rows_y[localRow],
    w: SVG_CFG.lotW,
    h: SVG_CFG.lotH,
  };
}

function get3DPosition(lot) {
  const r = getLotSVGRect(lot.zone, lot.num - 1);
  return { x: (r.x + r.w / 2) / 18, z: (r.y + r.h / 2) / 18 };
}

// ── SVG element helper ──
const SVG_NS = 'http://www.w3.org/2000/svg';
function svgEl(tag, attrs, text) {
  const e = document.createElementNS(SVG_NS, tag);
  Object.entries(attrs || {}).forEach(([k, v]) => e.setAttribute(k, String(v)));
  if (text !== undefined) e.textContent = text;
  return e;
}

